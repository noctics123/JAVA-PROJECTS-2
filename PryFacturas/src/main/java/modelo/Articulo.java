
package modelo;

public class Articulo {
   private String coda;
   private String desp;
   private double puni;
   private int stock;

    public String getCoda() {
        return coda;
    }

    public void setCoda(String coda) {
        this.coda = coda;
    }

    public String getDesp() {
        return desp;
    }

    public void setDesp(String desp) {
        this.desp = desp;
    }

    public double getPuni() {
        return puni;
    }

    public void setPuni(double puni) {
        this.puni = puni;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
   
}
